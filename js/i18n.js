let current = "hy";
export function setLang(l) { if (STRINGS[l]) current = l; }
export function getLang() { return current; }
export function t(key) { return (STRINGS[current] && STRINGS[current][key]) || STRINGS.en[key] || key; }
export function pickLangField(obj, prefix) {
  // obj has prefix_en / prefix_hy / prefix_fr; fall back to en.
  return obj[`${prefix}_${current}`] || obj[`${prefix}_en`] || "";
}
